    private void cargarTabla(String valor) {
        for (int i = 0; i < vc.listaEventos.size(); i++) {
            vc.listaEventos.get(i).setIndex(String.valueOf(i));
        }
        String[] titulos = {"Indice", "Fecha", "Hora", "Evento"};
        String[] registro = new String[4];

        dtmEventos = new DefaultTableModel(null, titulos);
        if (valor.equals("") || valor.equals(" ")) {
            for (Evento ev : vc.listaEventos) {
                registro[0] = String.valueOf(ev.getIndex());
                registro[1] = ev.getFecha();
                registro[2] = ev.getHora();
                registro[3] = ev.getEvento();
                dtmEventos.addRow(registro);
            }

            tablaEventos.setModel(dtmEventos);
        } else {

            for (Evento ev : vc.listaEventos) {
                if (ev.getEvento().contains(valor)||ev.getFecha().contains(valor)||ev.getHora().contains(valor)) {
                    registro[0] = String.valueOf(ev.getIndex());
                    registro[1] = ev.getFecha();
                    registro[2] = ev.getHora();
                    registro[3] = ev.getEvento();
                    dtmEventos.addRow(registro);
                }
            }
            tablaEventos.setModel(dtmEventos);
        }
    }

