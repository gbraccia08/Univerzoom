    private static void guardarInfo(ArrayList<Evento> list) {
        FileWriter writer = null;
        try {
            writer = (new FileWriter("Eventos.txt"));
            for (Evento evento : list) {
                writer.write(evento.toString());
            }
            writer.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }
