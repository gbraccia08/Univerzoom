    private static String imprimirEventos(ArrayList<Evento> list){
        String listaE ="";
        
        for(Evento e:list){
            listaE+="Hora: "+e.getHora()+"\nEvento: "+e.getEvento()+"\n";
        }
        return listaE;
    }
    
    private static String formatearFecha(Date date){
        long d = date.getTime();
        java.sql.Date fecha = new java.sql.Date(d);
        
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String strDate = dateFormat.format(fecha);
        
        return strDate;
    }
