    private static void cargarInfo(ArrayList<Evento> listaEventos) {
        try {
            File file = new File("Eventos.txt");

            FileReader fileReader = new FileReader(file);
            BufferedReader reader = new BufferedReader(fileReader);

            String line = null;

            while ((line = reader.readLine()) != null) {
                String[] ArrEventos = line.split(",");
                Evento ev = new Evento(ArrEventos[0], ArrEventos[1], ArrEventos[2], Integer.parseInt(ArrEventos[3]));
                listaEventos.add(ev);
            }
            reader.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private static void guardarInfo(Evento evento) {
        FileWriter writer = null;

        try {
            writer = new FileWriter("Eventos.txt",true);

            writer.write(evento.toString());
            writer.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }


